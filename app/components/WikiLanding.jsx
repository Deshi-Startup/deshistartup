function localHref(href) {
  const basePath = process.env.NEXT_PUBLIC_BASE_PATH || ''
  if (!href.startsWith('/')) return href
  if (!basePath) return href
  if (href === '/') return basePath || '/'
  return `${basePath}${href}`
}

